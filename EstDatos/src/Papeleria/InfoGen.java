package Papeleria;

public class InfoGen {
    String name;
    int ID;

    public InfoGen(String name, int ID) {
        setName(name);
        setID(ID);
    }
    
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

    public int getID() {
        return ID;
    }
    public void setID(int ID) {
        this.ID = ID;
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
}
